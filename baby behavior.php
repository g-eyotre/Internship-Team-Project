                        <p style="margin-left: 15px;"> It wouldn’t make sense that it could be hard to tell when a baby is hungry. If people needed a PhD to tell when babies needed to eat, babies wouldn’t survive. Babies will give parents lots of cues, called “clustered cues,” when they need them to do important things. A hungry newborn will move her head looking for something to suck on. She will pull her hands and her knees upward toward her face. She will make sucking noises and try to suck on anything she can find. If no one feeds her right away (babies don’t like to wait), she will start crying while still using all the other cues. Older babies will try to get into a breastfeeding position, or excitedly reach for the bottle or spoon. Babies use clustered cues to show they are full too. They relax their muscles, slow down in their eating, let their hands fall away from their face, and sometimes fall asleep. Making sure you know when to stop feeding is just as important to your baby as letting you know she needs to eat. It is important when parents hear their babies cry that they check for clustered hunger cues before they assume they are hungry. </p>
                                   
