                            <p style="margin-left: 15px;"> When babies want to interact with the people who love them (or anyone nearby), they will instinctively look, move, and make noise in specific ways. Collectively, these movements and noises are called “engagement cues.” </p>
                            <p style="margin-left: 15px;"> Your baby will have wide open eyes and look at you or a toy as if they are trying to memorize what they see. Their faces and their bodies will be relaxed and they will use smooth body movements. Older babies may smile and try to touch or taste whatever interests them. When they are very excited, babies will kick their legs and squirm with glee. </p>
                            <p style="margin-left: 15px;"> Using engagement cues, your baby is asking you to help her learn more about you and her new world. At first, your baby will be content just looking at your face and listening to your voice. Later, she’ll want to play more complicated games. Enjoy this time together but be prepared to watch for signs that your baby might be tiring. Engaging with you is hard work! </p>
                            
