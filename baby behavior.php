                           <p style="margin-left: 15px;"> When babies need a break, either for a moment or a nap, they’ll use a different set of movements and noises to make sure you know it. These signs are called “disengagement cues.” </p>
                           <p style="margin-left: 15px;"> Your baby may close his eyes, turn his face or body away from you or he may arch or twist his body away. His muscles will be tense and he may frown or look like he is about to cry. If he’s not allowed to take a break, he will start crying to make sure you know what to do. Older babies will stiffen their hands and bring them up towards their faces; they may try to change position, have you pick them up or put them down. </p>
