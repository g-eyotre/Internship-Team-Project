        <h4 style="margin-left: 180px; margin-top: 0.1px;">Answer;</h4>
            <p style="margin-left: 180px; margin-top: 0.1px;">They can begin by realizing that a major move can be hard on children. According to Parents magazine, "Toddlers will mourn the loss of their room and the house they’ve always lived in. School-age children will mourn the loss of their friends and their school. Teenagers, along with mourning all of the above, are likely to feel anger at their parents for a major life change that is beyond their control." With all this mourning going on, mom and dad have to be willing to allow their kids some time to grieve. So be patient. Eventually life will normalize again, but you can't expect it to happen overnight.</p><br><br>


            <h5 style="margin-left: 180px; margin-top: 0.1px;">How to help your kids cope</h5>
            <p style="margin-left: 180px; margin-top: 0.1px;">In her book After the Boxes are Unpacked, author Susan Miller suggests several ways of helping your kids make the transition as smoothly as possible.<br><br>

Don’t play down the importance of the changes they’re going through. Telling them that "everything will work out" isn't necessarily the best approach.<br><br>

Encourage them to express their fears and concerns. If they are too young to verbalize their thoughts, help them. If you have teenagers, ask them to express their feelings.<br><br>

Let them do the talking and make up your mind to be a good listener.<br><br>

Whatever you do, don’t deny your children’s feelings. That will only increase their sense of isolation and frustration.<br><br>

Don’t feel that you need to shoulder all the blame or justify the move to your kids. It’s not up to you to solve all their problems.<br><br>

Remember that it’s normal for some children to experience a temporary regression in behaviour after a move. If you have school-aged kids, you may even see a drop in their grades. If that’s the case, don’t panic. Give them the time and space they need in order to adjust to the new situation.</p>
