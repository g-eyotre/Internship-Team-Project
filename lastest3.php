                <ul>
  <li>Cry it out: A method that involves simply laying a baby in the crib, closing the door, and coming back to get them in the morning.</li>
  <li>Chair method: A method where the parent sits in a chair next to the child’s crib or bed and gradually moves the chair farther away until they are out of room. </li>
  <li>Pick up, put down: A method in which any time a baby is crying, the parent picks them up. As soon as the baby is calm, the parent returns them to the crib.</li> 
