            <h4 style="margin-left: 180px; margin-top: 0.1px;">Answer:</h4>
            <p style="margin-left: 180px; margin-top: 0.1px;">Depression is by far the most common and important emotional health problem in North America today – so common, in fact, that it might almost be compared with the common cold. Not only does it affect adults, but also children and adolescents, often with greater intensity. Clinical or major depression is a very serious problem and should be treated as such. Clinical depression is more than a temporary emotional slump. It involves a persistent – lasting two weeks or longer – and usually disruptive disturbance of mood and often affects other bodily functions as well. </p><br><br>



            <h5 style="margin-left: 180px; margin-top: 0.1px;">Recognizing characteristics</h5>
            <p style="margin-left: 180px; margin-top: 0.1px;">Here’s a list of the most salient characteristics of the condition:<br><br>

Persistent sadness and/or irritability. This may include mood swings that seem out of proportion to the circumstances; episodes of moping and crying; withdrawal and isolation; fatigue and loss of enthusiasm or interest in favourite activities; poor school performance; and outbursts of anger and overt acting out.<br><br>
Painful thoughts that manifest themselves in relentless introspection, a negative self-concept, persistent anxiety and a sense of hopelessness.<br><br>
Physical symptoms such as insomnia, changes in appetite, headaches, dizziness, nausea, heart palpitations, abdominal cramps and episodes of shortness of breath.<br><br>
In rare cases, a severe case of depression may also involve delusional thinking, including visual and auditory hallucinations. This is not merely depression but a form of psychosis, a serious disorder of neurochemical functions in the brain.<br><br>
As you might expect, the causes of depression can be extremely complex, including a blend of genetic, biochemical, personal, family and spiritual factors. </p><br><br>


            <h5 style="margin-left: 180px; margin-top: 0.1px;">Recognizing predictors of suicide </h5>
            <p style="margin-left: 180px; margin-top: 0.1px;">As you are apparently aware, many of the unique features of depression among young people also increase the risk of suicide. In particular, the intensity of their emotions and a shortage of life experiences may give rise to impulsive self-destructive behaviour. You should be especially concerned about this possibility if any of the following predictors of suicide are present:<br><br>

A previous suicide attempt.<br><br>
A family history of suicide.<br><br>
Expressions of intense guilt or hopelessness.<br><br>
Threatening, talking or joking about suicide.<br><br>
"Cleaning house" – i.e., a sudden impulse to give away personal possessions.<br><br>
Suicide among other adolescents in your community.<br><br>
A sudden, major loss or humiliation. Parents should be on the alert to ensure that their child is not the victim of either physical or social bullying.<br><br>
If, on the basis of this information, you have reason to suspect that your adolescent might be seriously depressed or suicidal, seek appropriate help immediately. You may want to contact your physician for advice or a referral. </p><br><br>


            <h5 style="margin-left: 180px; margin-top: 0.1px;">Deal with the depression</h5>
            <p style="margin-left: 180px; margin-top: 0.1px;">Even if a present threat of suicide doesn’t seem to be part of the picture, you should still take definite steps to deal with the depression. Listen carefully to your son when he talks about his problems. Take his feelings seriously. Pray with him and for him. Get a physician’s evaluation of his condition and be willing to consider appropriate medication (antidepressants can normalize disturbances in neurotransmitter function in the brain and are neither addictive nor an "escape from reality").<br><br> 

