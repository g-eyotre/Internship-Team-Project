            <h4 style="margin-left: 180px; margin-top: 0.1px;">Answer</h4>
            <p style="margin-left: 180px; margin-top: 0.1px;">The answer to your first question is yes: your experience is quite normal and extremely common. Between 50 and 80 per cent of new moms are affected by a temporary emotional slump popularly known as "baby blues," while about 10 per cent suffer from a more severe disturbance known as postpartum depression. A much less common – but far more intensive – disturbance described as postpartum psychosis occurs after about one in 1,000 deliveries.<br><br> 
In view of the intense physical and psychological changes that accompany the birth of a baby, it’s actually surprising that storm clouds aren’t a part of every mother’s emotional weather in the weeks immediately following childbirth. There are many physical factors that can affect a woman’s emotions at this time, including hormonal shifts, physical exhaustion, pain, blood loss, lack of sleep and a wide variety of genetic considerations.</p><br><br>
<h5 style="margin-left: 180px; margin-top: 0.1px;">Postpartum "blues"</h5>
<p style="margin-left: 180px; margin-top: 0.1px;">Postpartum "blues," the most common mood problem related to childbirth, usually develops during the first week after delivery. Symptoms can include irritability, tearfulness, anxiety, insomnia, lack of energy, loss of appetite and difficulty concentrating. While this emotional and physical slump typically resolves itself within two weeks, it should not be met with an attitude of "ignore it and it will go away." Emotional support and practical assistance from husband, family and friends are extremely important to any woman suffering from the "baby blues."</p><br><br>
<h5 style="margin-left: 180px; margin-top: 0.1px;">Postpartum depression</h5>
<p style="margin-left: 180px; margin-top: 0.1px;">Postpartum depression (PPD) is a more serious condition. It can begin at any time during the first six months after childbirth and may last for several months. A mother with PPD may be so intensely depressed that she has difficulty caring for her baby, or she may develop extreme unrealistic anxiety over the infant’s health. While the problem can resolve itself in time, this does not mean that it should be left to run its course. Like a major depression at any other season of life, PPD is not a problem that can be solved by means of a little attitude adjustment. If symptoms continue for more than two weeks, professional help should be sought. Treatment might involve extended counselling, the use of antidepressant medication or both. If a mother is breastfeeding, input from the baby’s doctor will be needed before initiating drug therapy.<br><br> 
If you feel you might be suffering from PPD, we’d encourage you to give our counselling department a call. You can reach us at @smartmumuganda. Our counsellors will be happy to discuss your situation with you over the phone. They can also provide you with a list of qualified counsellors in your area. </p><br><br>
<h5 style="margin-left: 180px; margin-top: 0.1px;">Postpartum psychosis</h5>
<p style="margin-left: 180px; margin-top: 0.1px;">In the relatively rare but very serious disorder called postpartum psychosis, a woman experiences not only a disturbance of mood but also a break with reality. The condition may include hallucinations, delusions, suicidal thoughts and even violent behaviour. Current research indicates that five per cent of women with postpartum psychosis kill themselves, and an equal percentage kill their baby. Because of this risk, postpartum psychosis should be considered a medical emergency and must be evaluated immediately by a qualified psychiatrist. The condition can occur without warning, and there is a 30 to 50 per cent chance that it will recur with subsequent pregnancies. The good news is that it can be effectively treated with appropriate medication.</p><br><br>

