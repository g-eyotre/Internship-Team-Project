            <h5 style="margin-left: 180px; margin-top: 0.1px;">PACKING FOR FAMILY TRAVEL</h5>
            <p style="margin-left: 180px; margin-top: 0.1px;">Packing for family travel involves even more consideration than packing for adults. Not only does this mean more luggage overall, but also adding in those child and baby specific items that you require for your trip.<br><br>

Depending on the age of your children and the type of transport you are using for your trip, you will need to ensure you are aware of luggage allowances and restrictions.<br><br>

If your child is under 2, in most cases the only luggage allowance when flying is a carry on bag with essentials and no actual checked luggage included.<br><br>

For most other transport, allowances are either not specified or are less strict.<br><br>

The best method for packing for family travel is to utilise a travel packing checklist. There are plenty of printable packing lists that suit different types of travel, from general travel, to more specific checklists aimed at certain destinations or travelling with kids.<br><br>

Items such as disposable nappies and formula are bulky. Cloth nappies may take up less space, however, you need to ensure you will have washing facilities at your destination.<br><br>

Feeding supplies can also take up a lot of room, such as baby bottles, plates and cutlery. You may need a sterilisation system as well, so be sure to consider travel alternatives.<br><br>

If you are sharing a suitcase with your children or partner, a great method is to utilise suitcase organisers or packing cubes – they are smaller soft bags that zip up and can be a great way to separate different types of clothing for different family members. They also help compact your luggage better too.<br><br>

Make sure you have extra sets of clothing for kids. No matter their age, kids are great at getting messy so you don’t want to run out of clean clothes without having easily accessible washing facilities.<br><br>

As a rule of thumb, pack at least two extra outfits for kids compared to what you are packing for yourself.<br><br>

Don’t forget the anywhere essentials such as a warm jacket, a hat and good quality walking shoes. Keeping kids warm, sun-safe and comfortable will make your family travel much more enjoyable.</p>





